function loadMenuData() {
  const pageUrl = window.location.pathname;
  let menuData;
  if (pageUrl.includes("velvet.html")) {
    menuData = velvetData;
  } else if (pageUrl.includes("eclipse.html")) {
    menuData = eclipseData;
  } else if (pageUrl.includes("celestial.html")) {
    menuData = celestialData;
  } else {
    console.error("Kein Menü gefunden für diese Seite");
    return;
  }
  const menuContainer = document.getElementById("menu");
  if (menuContainer) {
    menuContainer.innerHTML = renderMenu(menuData[0].menu);
  }
}

// Neue Funktion für das Kachel-Design der Restaurant-Menüs
function generateMenuCards() {
    const currentPage = window.location.pathname;
    let restaurantData;
    
    if (currentPage.includes('velvet.html')) {
        restaurantData = velvetData[0];
    } else if (currentPage.includes('eclipse.html')) {
        restaurantData = eclipseData[0];
    } else if (currentPage.includes('celestial.html')) {
        restaurantData = celestialData[0];
    } else {
        return; // Nicht auf einer Restaurant-Seite
    }
    
    // Vorspeisen rendern
    const startersContainer = document.getElementById('starters_menu');
    if (startersContainer && restaurantData.menu.Vorspeisen) {
        startersContainer.innerHTML = '';
        restaurantData.menu.Vorspeisen.forEach(dish => {
            startersContainer.innerHTML += generateDishCard(dish);
        });
    }
    
    // Hauptspeisen rendern
    const mainContainer = document.getElementById('main_menu');
    if (mainContainer && restaurantData.menu.Hauptspeisen) {
        mainContainer.innerHTML = '';
        restaurantData.menu.Hauptspeisen.forEach(dish => {
            mainContainer.innerHTML += generateDishCard(dish);
        });
    }
    
    // Nachspeisen rendern
    const dessertContainer = document.getElementById('dessert_menu');
    if (dessertContainer && restaurantData.menu.Nachspeisen) {
        dessertContainer.innerHTML = '';
        restaurantData.menu.Nachspeisen.forEach(dish => {
            dessertContainer.innerHTML += generateDishCard(dish);
        });
    }
}

function generateDishCard(dish) {
    return `
        <div class="dish_card">
            <img src="${dish.img}" alt="${dish.alt}" class="dish_image">
            <div class="dish_info">
                <h3 class="dish_name">${dish.name}</h3>
                <p class="dish_description">${dish.desc}</p>
                <div class="dish_footer">
                    <span class="dish_price">${dish.price.toFixed(2)} €</span>
                    <button class="add_button" onclick="addToCart('${dish.name}', ${dish.price})">
                        Hinzufügen
                    </button>
                </div>
            </div>
        </div>
    `;
}

// Auto-Load für die neuen Menüs
document.addEventListener('DOMContentLoaded', function() {
    generateMenuCards();
});
